#include <controllers/plugins/KSQPluginsController.h>

//-----------------------------------------------------------------------------
bool
KSQPluginsController::load(QSharedPointer<KSQOneExtension> extension) {
    QString pluginDir(extension->basePath());
    if (!m_engine) {
        VS_LOG_WARNING("QML Engine is not set");
        return false;
    }
    // Create JS processor
    const QString js = pluginDir + "/src/js/main.qml";
    QQmlComponent component(m_engine, QUrl(js));
    if (component.isError()) {
        qDebug() << component.errors();
    }
    QObject *object = component.create();
    if (!object) {
        VS_LOG_ERROR("Cannot create QML processor for device type : %s", pluginDir.toStdString().c_str());
        return false;
    }
    m_qmlProcessor[0].reset(object);

    // Set JS processor for extension
    extension->setJs(object);

    // Inform JS code about finish of plugin load
    QMetaObject::invokeMethod(object, "onLoad", Qt::QueuedConnection);

    return true;
}

