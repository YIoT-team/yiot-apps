#ifndef PROVISION_QT_WIFI_MACOS_H
#define PROVISION_QT_WIFI_MACOS_H

#include <QtCore>

struct KSWiFiInfo {
    int rssi;
    QDateTime lastUpdate;

    KSWiFiInfo(int _rssi) {
        rssi = _rssi;
        lastUpdate = QDateTime::currentDateTime();
    }

    KSWiFiInfo() {
        rssi = -100;
        lastUpdate = QDateTime::currentDateTime().addYears(-1);
    }
};

typedef QMap<QString, KSWiFiInfo> KSQWiFiNetworks;

#if defined(Q_OS_MACOS) || defined(Q_OS_IOS) || defined(Q_OS_LINUX) || defined(Q_OS_WIN32)
KSQWiFiNetworks
wifi_enum();
#endif

#if defined(Q_OS_IOS)
QString
wifi_ssid();

void
wifi_settings();
#endif

#endif // PROVISION_QT_WIFI_MACOS_H
